  <p style="font-size:19px; text-align: justify; padding: 5%;">According to early accounts, the present town site of Balingasag was popularly known as Gumpot, while Balingasag use to be the place which is now Galas. Long ago, the territory of Galas was ruled by Datu Marcos and his wife, Ba-i Gregoria while Gumpot territory was under the rule ship of Datu Mateo and his wife, Ba-i Tomasa. The territory of Datu Marcos was frequently inundated by the overflowing waters from the Balatukan River, so the natives moves northward toward Gumpot. They finally settled in Gumpot and called the new settlement as Balingasag and the name had struck since then. Then name Balingasag was derived from the words "baling" which means fishing net and "kasag" means crab. Legend says that during the Spanish regime, there was a group of civil guards patrolling the seashore. Upon reaching the cave near Galas they saw se fishermen by the shore who were pulling their fishnets. The guards asked for the name of the Poblacion but the men did not understand their language and thought they were asked what they were doing so they answered "baling", pointing to the net, and "kasag", pointing the crabs in their bancas. The Spaniards upon hearing the words "baling" and "kasag" repeatedly said the words until the other civil guards picked up the sound and repeated after them "baling-kasag". Thus the place was given a name "Balingasag" which appeared in official records of the Spaniards. Mamerto Manuel Valmores, grandson of Datu Mateo of Gumpot and Antonio Ramon Madroño, grandson of Datu Marcos of Galas were the founders of this town. Both were given the title of Capitanes in 1842. Antonio, however, was a retiring man and entrusted the job of running the affairs of the town to Mamerto. Thus, Mamerto became the first Capitan Municipal during the Spanish regime.</p>
